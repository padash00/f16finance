// ─── ACTION TRIGGERS ──────────────────────────────────────────────────────────

export function buildActionPrompt(flags: {
  lowMargin: boolean
  expenseTrendUp: boolean
  highConcentration: boolean
  negativeDaysCount: number
}): string {
  if (!flags.lowMargin && !flags.expenseTrendUp && !flags.highConcentration && flags.negativeDaysCount === 0) {
    return ''
  }

  const lines = [
    'АВТОМАТИЧЕСКИЕ ТРИГГЕРЫ (посчитаны сервером, учти в ответе):',
  ]

  if (flags.lowMargin) {
    lines.push('⚠️ ТРИГГЕР: Маржа < 10% → 3 антикризисных действия: (1) сократить топ-расход, (2) поднять средний чек/цену, (3) контроль утечек/косяков смен.')
  }
  if (flags.expenseTrendUp) {
    lines.push('⚠️ ТРИГГЕР: Расходы растут → 1 действие на закуп/категории, 1 на лимиты/процессы, 1 на контроль отклонений.')
  }
  if (flags.highConcentration) {
    lines.push('⚠️ ТРИГГЕР: Концентрация выручки > 60% на одной точке → 1 действие на диверсификацию, 1 на риск-план, 1 на перенос лучших практик.')
  }
  if (flags.negativeDaysCount > 0) {
    lines.push(`⚠️ ТРИГГЕР: ${flags.negativeDaysCount} убыточных дней → 1 действие на расписание расходов, 1 на минимальный дневной план выручки, 1 на контроль кассы.`)
  }

  return lines.join('\n')
}


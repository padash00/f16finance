import 'server-only'

/**
 * Нейтрализует символы, которыми пользовательский ввод может «вырваться» из условия
 * PostgREST `.or()` / `.filter()` и подменить логику запроса (инъекция).
 *
 * - `,` `(` `)` `.` — разделяют условия/операторы PostgREST → заменяем на пробел;
 * - `%` `_` `\` — wildcards LIKE → экранируем.
 *
 * Легитимные значения (телефоны, номера карт, коды станций — буквы+цифры) проходят
 * без изменений. Используется для всех `.or(...)`, куда попадает свободный ввод.
 */
export function sanitizeOrFilterValue(value: unknown, maxLen = 100): string {
  return String(value ?? '')
    .slice(0, maxLen)
    .replace(/[%_\\]/g, '\\$&')
    .replace(/[,().]/g, ' ')
    .trim()
}

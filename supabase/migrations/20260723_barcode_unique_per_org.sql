-- ─────────────────────────────────────────────────────────────────────────────
-- Изоляция арендаторов, класс 1: штрихкод товара уникален В ПРЕДЕЛАХ организации,
-- а не глобально. Глобальный уникальный индекс позволял одному арендатору
-- «угнать» или перезаписать товар другого по совпавшему штрихкоду (upsert
-- onConflict:'barcode') и ломал импорт каталога (23505 на чужой barcode).
--
-- Безопасно на текущей базе: barcode пока глобально уникален → пара
-- (organization_id, barcode) уникальна тривиально, конфликтов при создании нет.
-- Идемпотентно.
-- ─────────────────────────────────────────────────────────────────────────────

drop index if exists public.inventory_items_barcode_uidx;

-- NULL organization_id (легаси-строки общего каталога) Postgres считает
-- различными — на них ограничение не действует, что сохраняет прежний
-- «общий» каталог до бэкфилла. Свои товары арендатора — строго уникальны.
create unique index if not exists inventory_items_org_barcode_uidx
  on public.inventory_items (organization_id, barcode);

notify pgrst, 'reload schema';
